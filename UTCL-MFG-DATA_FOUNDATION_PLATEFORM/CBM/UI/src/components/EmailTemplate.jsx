import axios from "axios";
import { AuthenticatedTemplate } from "@azure/msal-react";
import { useEffect, useState } from "react";
import { config } from "../config";
import Select from "react-select";
import "../styles/EmailTemplate.css";
import Loader from "react-js-loader";
import { toast } from "react-toastify";
import { CKEditor } from "@ckeditor/ckeditor5-react";
import ClassicEditor from "@ckeditor/ckeditor5-build-classic";
import { useLocation, useNavigate } from "react-router-dom";
import { BiChevronLeft } from "react-icons/bi";
import { accessLogApi } from "../utils/AccessLogApi";
export const EmailTemplate = () => {
  const loader = (
    <Loader
      type="spinner-circle"
      bgColor={"#000000"}
      title={"bubble-scale"}
      color={"#FFFFFF"}
      size={50}
    />
  );
  const { state } = useLocation();
  const navigate = useNavigate();
  const [submitLoader, setSubmitLoader] = useState(false);
  const [TemplateOptions, setTemplateOptions] = useState([]);
  const [EmailData, setEmailData] = useState({
    subject: "",
    TempletName: "",
    useCaseTemplate: "",
  });
  const [recipient, setRecipient] = useState([]);
  const [recipientInput, setRecipientInput] = useState("");
  const [recipientError, setRecipientError] = useState(null);
  const [EditorData, setEditorData] = useState(
    `<html><head><style>table, th, td {{border: 1px solid black;border-collapse: collapse}}</style></head><body><b>Description</b>:<br>Following information generated by automated system.<br><b>{fan_name} - Fan Efficiency gone down by {critical_percent:.1f}% at {current_time}</b><br>Critical parameters listed below with its instantaneous value when the exception has been raised<br><br><br><table><tr><th>Parameters</th><th>Values</th></tr><tr><td><FONT COLOR="RED">Fan Efficiency</td><td><FONT COLOR="RED">{current_efficiency:.3f}%</td></tr><tr><td>Fan Speed</td><td>{current_speed:.2f} RPM</td></tr><tr><td>Fan load</td><td>{current_load:.2f} KW</td></tr><tr><td>Fan Inlet Draft</td><td>{current_inlet:.2f}</td></tr><tr><td>Fan Outlet Draft </td><td>{current_outlet:.2f}</td></tr><tr><td>Fan Temperature</td><td>{current_temp:.2f}℃</td></tr></body></html>`
  );

  const setTemplateData = () => {
    let EditData = state?.rowdata;
    if (state?.copyData) {
      EditData = state?.copyData;
      EditData.EmailTemplateName = EditData.EmailTemplateName + "- Copy";
    }
    let Temp = { ...EmailData };

    Temp.TempletName = EditData.EmailTemplateName;
    Temp.subject = EditData.Subject;
    Temp.useCaseTemplate = EditData.UseCaseTemplate;

    setRecipient(EditData.Recipients);
    EditData.Description = EditData.Description.replaceAll("@@@", ",");

    setEditorData(EditData.Description.split("</style>").pop());

    setEmailData(Temp);
    state.EditFlag = false;
  };

  useEffect(() => {
    accessLogCBM()
    if (
      state?.rowdata === undefined &&
      window.location.href.split("/").pop() === "editEmailTemplate"
    ) {
      navigate("/EmailTemplateList");
    }
    if (
      TemplateOptions.length > 0 &&
      (state?.rowdata || state?.copyData) &&
      state.EditFlag
    ) {
      // console.log("here2222222");
      setTemplateData();
    }
  }, [TemplateOptions]);

  const accessLogCBM = () => {
    let payload = {
      userId: JSON.parse(sessionStorage.getItem("accountDetails"))?.username,
      ip: "",
      module: "CBM",
      plant: "-",
      url: window.location.href,
      options: "",
      plant_or_section: "CBM",
    };
    accessLogApi(payload)
  }

  const getTemplateData = () => {
    let payload = {
      userid: JSON.parse(sessionStorage.getItem("accountDetails"))?.username,
    };
    axios.post(config.BaseUrl + "/cbm/template/getTemplateData", payload, {
      headers: {
        Authorization: `Bearer ${JSON.parse(localStorage.getItem("login"))?.token
          }`,
      },
    })
      .then((res) => {
        res = res.data.result;
        if (res === "No Records Found") {
          setTemplateOptions([]);
        } else {
          let TempList = {};
          res.map((val) => {
            TempList[val.TemplateName + val.Timestamp] = {
              value: val.TemplateName,
              label: val.TemplateName + " UseCase : " + val.UseCase,
              Timestamp: val.Timestamp,
            };
          });
          TempList = Object.values(TempList);

          setTemplateOptions(TempList);
        }
      })
      .catch((err) => {
        toast.error(err.message);
      });
  };
  const handleChangeRecipientInput = (event) => {
    setRecipientInput(event.target.value);
    setRecipientError("");
  };
  const handleDeleteEmail = (item) => {
    let tempRecipient = [...recipient];
    tempRecipient = tempRecipient.filter((i) => i !== item);
    setRecipient(tempRecipient);
  };
  const handleKeyDownRecipient = (event) => {
    if (["Enter", "Tab", ","].includes(event.key)) {
      event.preventDefault();

      var value = recipientInput.trim();
      if (!/[\w\d\.-]+@[\w\d\.-]+\.[\w\d\.-]+/.test(value)) {
        setRecipientError("Invalid Email");
      } else {
        if (recipient.includes(value)) {
          setRecipientError("Already in List");
        } else {
          let trec = [...recipient];
          trec.push(value);
          setRecipient(trec);
          setRecipientInput("");
        }
      }
    }
    // setRecipientInput(event.target.value);
  };

  const handleInput = (e) => {
    let tempEmaildata = { ...EmailData };
    tempEmaildata[e.target.name] = e.target.value;
    setEmailData(tempEmaildata);
  };
  const handleChangeSelect = (e) => {
    let tempEmaildata = { ...EmailData };
    tempEmaildata.useCaseTemplate = e.value;
    tempEmaildata.TempletName = e.value;
    tempEmaildata.subject = e.value;
    setEmailData(tempEmaildata);

  };
  useEffect(() => {
    getTemplateData();
  }, []);

  const SendData = () => {
    if (EmailData.useCaseTemplate === "") {
      toast.error("Please Select UseCase Template");
      return;
    }
    if (EmailData.subject === "") {
      toast.error("Please Enter Subject");
      return;
    }
    if (recipient.length === 0) {
      toast.error("Please Enter recipient");
      return;
    }

    let postUrl = config.BaseUrl + "/cbm/template/sendEmailTemplateData";
    let payload = {
      userid: JSON.parse(sessionStorage.getItem("accountDetails"))?.username,
      EmailTemplateName: EmailData.TempletName,
      Subject: EmailData.subject,
      UseCaseTemplate: EmailData.useCaseTemplate,
      Recipients: recipient,
      Description: ` <style>table, th, td {{border: 1px solid black;border-collapse: collapse}}</style>${EditorData}`,
    };

    if (state?.rowdata) {
      postUrl = config.BaseUrl + "/cbm/template/editEmailTemplateData";
      payload.TimeStamp = state.rowdata.Timestamp;
    }
    setSubmitLoader(true);
    axios
      .post(postUrl, payload, {
        headers: {
          Authorization: `Bearer ${JSON.parse(localStorage.getItem("login"))?.token
            }`,
        },
      })
      .then((res) => {
        res = res.data.result;

        if (state?.rowdata) {
          toast.success("Template Updated Successfully.");
        } else {
          toast.success("Template Added Successfully.");
        }
        setSubmitLoader(false);
        navigate("/EmailTemplateList");
      })
      .catch((err) => {
        toast.error(err.message);
        setSubmitLoader(false);
      });
  };
  return (
    <AuthenticatedTemplate>
      <div className="w-full h-full">

        {/* HEADER */}
        <div className="flex justify-between items-center mb-2 -mt-2">
          <h2 className="flex text-[18px] font-medium font-poppins text-[var(--title)] -mt-1.5 -ml-0.5">
            Email Template
          </h2>
          <button
            onClick={() => navigate("/EmailTemplateList")}
            className="border !border-[var(--button-border)] !bg-[var(--button-bg)] text-[14px] font-medium px-2 py-1 rounded-lg transition cursor-pointer hover:!bg-[var(--button-hover-bg)] inline-flex items-center gap-1 color text-[var(--text-color)]"
          >
            {/* <BiChevronLeft size={20} /> */}
            Back
          </button>
        </div>

        {/* CARD CONTAINER */}
        <div className="
        w-full
        rounded-2xl
        border border-[var(--search-border)]
        shadow-[var(--card-shadow)]
        py-2 px-2.5
      ">

          {/* FORM GRID */}
          <div className="grid grid-cols-1 md:grid-cols-2 gap-3">

            {/* USE CASE */}
            <div className="flex flex-col">
              <label className="text-sm font-medium text-[var(--text-color)]">
                Select UseCase Template <span className="text-red-500">*</span>
              </label>

              <Select
                value={
                  EmailData.useCaseTemplate !== "" &&
                  TemplateOptions.filter(
                    (item) => item.value === EmailData.useCaseTemplate
                  )
                }
                placeholder="Select Template"
                onChange={(e) => handleChangeSelect(e)}
                options={TemplateOptions}
                classNamePrefix="select"
                styles={{
                  control: (base) => ({
                    ...base,
                    backgroundColor: "var(--report-field-bg-color)",
                    borderColor: "var(--form-border)",
                  }),
                  menu: (base) => ({
                    ...base,
                    backgroundColor: "var(--report-field-bg-color",
                  }),
                }}
              />
            </div>

            {/* TEMPLATE NAME */}
            <div className="flex flex-col">
              <label className="text-sm font-medium text-[var(--text-color)]">
                Email Template Name <span className="text-red-500">*</span>
              </label>

              <input
                name="TempletName"
                value={EmailData.TempletName}
                onChange={handleInput}
                disabled={!state?.copyData}
                className="
                w-full
                rounded-lg
                border border-[var(--form-border)]
                h-[38px]
                bg-[var(--report-field-bg-color)]
                px-3 py-2
                text-[var(--text-color)]
                focus:outline-none
                focus:ring-2
                focus:ring-[var(--primary-color)]
              "
              />
            </div>

            {/* SUBJECT */}
            <div className="flex flex-col">
              <label className="text-sm font-medium text-[var(--text-color)]">
                Mail Subject <span className="text-red-500">*</span>
              </label>

              <input
                name="subject"
                value={EmailData.subject}
                onChange={handleInput}
                className="
                w-full
                rounded-lg
                border border-[var(--form-border)]
                h-[38px]
                bg-[var(--report-field-bg-color)]
                px-3 py-2
                text-[var(--text-color)]
                focus:outline-none
                focus:ring-2
                focus:ring-[var(--primary-color)]
              "
              />
            </div>

            {/* RECIPIENTS */}
            <div className="flex flex-col">
              <label className="text-sm font-medium text-[var(--text-color)]">
                Recipients <span className="text-red-500">*</span>
              </label>

              <div className="flex flex-wrap">
                {recipient.map((item) => (
                  <div
                    key={item}
                    className="
                    rounded-md
                    text-sm
                    bg-[var(--button-bg)]
                    border border-[var(--form-border)]
                  "
                  >
                    {item}
                  </div>
                ))}
              </div>

              <input
                value={recipientInput}
                onKeyDown={handleKeyDownRecipient}
                onChange={handleChangeRecipientInput}
                placeholder="Type email and press Enter"
                className="
                w-full
                rounded-lg
                border border-[var(--form-border)]
                h-[38px]
                bg-[var(--report-field-bg-color)]
                px-3 py-2
                text-[var(--text-color)]
                focus:outline-none
                focus:ring-2
                focus:ring-[var(--primary-color)]
              "
              />
            </div>
          </div>

          {/* NOTE SECTION */}
          <div className="mt-4 text-sm text-[var(--text-color)] space-y-2">
            <p>
              <strong>Note :</strong> Available Dynamic Parameters are as below.
              Include them like <span className="font-medium">{`{critical_limit}`}</span>
            </p>

            <div className="flex flex-wrap gap-2">
              {[
                "plant_name",
                "fan_name",
                "critical_limit",
                "timestamp",
                "current_efficiency",
                "current_speed",
                "current_load",
                "current_inlet",
                "current_outlet",
                "current_temp",
                "critical_percent",
              ].map((param) => (
                <span
                  key={param}
                  className="
                  px-2 py-1
                  rounded-md
                  text-xs
                  bg-[var(--table-row-hover)]
                  border border-[var(--form-border)]
                "
                >
                  {param}
                </span>
              ))}
            </div>
          </div>

          {/* TEMPLATE BODY */}
          <div className="mt-8">
            <label className="text-sm font-medium text-[var(--text-color)]">
              Template Body
            </label>

            <div className="mt-2 border border-[var(--form-border)] rounded-lg overflow-hidden">
              <CKEditor
                editor={ClassicEditor}
                data={EditorData}
                onChange={(event, editor) =>
                  setEditorData(editor.getData())
                }
              />
            </div>
          </div>

          {/* SUBMIT BUTTON */}
          <div className="flex justify-center items-center mt-4">
            <button
              disabled={EmailData.TempletName === "" || submitLoader}
              onClick={SendData}
              className="
    h-[32px] w-fit text-[14px] shrink-0 cursor-pointer px-2 rounded-md bg-[var(--submit-button-bg)] hover:bg-[var(--submit-button-hover-bg)] text-[#111111] text-sm font-medium transition-all duration-200
  "
            >
              {state?.rowdata ? "Update Template" : "Submit Template"}
            </button>
          </div>
        </div>
      </div>
    </AuthenticatedTemplate>
  );
};
